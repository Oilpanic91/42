#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	char	*buffer;

	if (!s1 && !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen(s1);
	while (i && ft_strchr(set, s1[i]))
		i--;
	buffer = ft_substr((char *)s1, 0, i + 1);
	if (buffer == NULL)
		return (NULL);
	return (buffer);
}
