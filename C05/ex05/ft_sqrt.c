int	ft_sqrt(int nb)
{
	int a;

	a = 0;
	if (nb < 0 && nb == 0)
		return (0);
	while (a * a < nb && (a * a) >= 0)
		a++;
	if (a * a == nb)
		return (a);
	else
		return (0);
}
